$((function() {
    window.landing = new Landing
}
)),
$(window).on("load", (function() {}
)),
$(window).on("resize", (function() {}
));
class Landing {
    constructor() {
        const e = this;
        e.$list = $(".js-exhi-list"),
        e.$item = $(".js-exhi-item"),
        "scrollRestoration"in history && (history.scrollRestoration = "manual"),
        window.addEventListener("beforeunload", (function() {
            window.scrollTo(0, 0)
        }
        )),
        e.init()
    }
    init() {
        const e = this;
        e.handleList(),
        e.handleKV(),
        e.handleKvVideo(),
        e.handleScroll()
    }
    handleList() {
        const e = this;
        $(".js-exhi-item.active").length || e.$item.first().addClass("active"),
        e.$item.click((function(t) {
            if ($(this).hasClass("active"))
                return;
            t.preventDefault();
            let i = e.$list.height();
            e.$list.height(i),
            e.$item.removeClass("active"),
            $(this).addClass("active"),
            setTimeout(( () => {
                e.$list.height("auto")
            }
            ), 500)
        }
        ))
    }
    handleKV() {
        let e = $(".js-kv-body");
        $(".js-kv").on("mousemove", (function(t) {
            var i = ($(this).innerWidth() / 2 - t.pageX) / 90
              , o = ($(this).innerHeight() / 2 - t.pageY) / -30;
            i = i > 7 ? 7 : i < -7 ? -7 : i,
            o = o > 11 ? 11 : o < -11 ? -11 : o,
            e.attr("style", "transition: none;transform: rotateY(" + i + "deg) rotateX(" + o + "deg);-webkit-transform: rotateY(" + i + "deg) rotateX(" + o + "deg);-moz-transform: rotateY(" + i + "deg) rotateX(" + o + "deg)")
        }
        )),
        $(".js-kv").on("mouseleave", (function(t) {
            e.css({
                transition: "0.8s ease",
                transform: "none"
            })
        }
        ))
    }
    handleScroll() {
        const e = this
          , t = gsap.matchMedia();
        gsap.registerPlugin(ScrollTrigger);
        const i = {
            trigger: ".landing-hero",
            start: "100px top",
            end: "bottom top",
            scrub: !0,
            overscroll: !1,
            onEnter: () => {
                $("#kv-video").get(0).pause(),
                $("#kv-video").get(0).currentTime = 7
            }
            ,
            onLeaveBack: () => {
                $("#kv-video").get(0).play()
            }
        };
        gsap.registerPlugin(ScrollTrigger),
        t.add("(min-width: 992px)", ( () => {
            e.scrollVideo = gsap.to("#kv-video", {
                scale: 4,
                opacity: .8,
                scrollTrigger: {
                    ...i
                }
            })
        }
        )),
        t.add("(max-width: 992px)", ( () => {
            e.scrollVideo = gsap.to("#kv-video", {
                scale: 1.85,
                opacity: .65,
                filter: "blur(13px)",
                scrollTrigger: {
                    ...i,
                    onEnter: () => {
                        $("#kv-video").get(0).pause(),
                        $("#kv-video").get(0).currentTime = 0
                    }
                }
            })
        }
        )),
        window.jumpTo = window.scrollTo,
        window.scrollTo = () => {}
    }
    handleKvVideo() {
        const e = $("#kv-video").get(0);
        isSafari() && (e.src = "video/kv_safari.mp4",
        e.type = "video/mp4")
    }
}
